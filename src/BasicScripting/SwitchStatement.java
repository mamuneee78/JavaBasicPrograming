package BasicScripting;

public class SwitchStatement {
	
	public static void main(String[]args)
	{
		int day = 5;
		switch (day) {
		case 2:
			System.out.println("two");
			break;
			
		case 3:
			System.out.println("three");
			break;
			
		case 4:
			System.out.println("four");
			break;
			
		case 5:
			System.out.println("five");
			break;
		
		
		case 1:
			  
		    System.out.println("One");
		    break;
	}
	
	}

}
