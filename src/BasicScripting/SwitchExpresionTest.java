package BasicScripting;

public class SwitchExpresionTest {

	public static void main(String[] args) {
		int month = 5;
		switch (month) {
		case 2:
			System.out.println("Two");
			break;
			
		case 4:
			System.out.println("Four");
			break;
			
		case 5:
			System.out.println("FIVE");
			break;
			
		case 7:
			System.out.println("SEVEN");
			break;
		
		}

	}

}
